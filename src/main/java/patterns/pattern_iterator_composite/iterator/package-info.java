package patterns.pattern_iterator_composite.iterator;
/*
The Iterator Pattern provides a way to access the elements of an
aggregate object sequentially without exposing its underlying representation

- Java supports iterator implementations in some of its collection oriented classes (e.g. ArrayList)

- The for-each loop is the "new" (Java 5) way to iterate over collections instead of using an iterator object

- An iterator allows access to an aggregate's element without exposing its internal structure

*/